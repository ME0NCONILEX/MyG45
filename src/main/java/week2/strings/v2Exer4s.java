package week2.strings;
//Strings - convert lowercase <--> uppercase
public class v2Exer4s {
    public static void main(String[] args) {
        String myTxt = ("CAPS EQUALS SCREAMING");
        System.out.println("The original sentence: \n" + myTxt);
        System.out.println("... converted to lowercase: \n" + myTxt.toLowerCase());
        System.out.println("... and back to uppercase: \n" + myTxt.toLowerCase());
    }
}
